'use client';

import { useMemo } from 'react';
import Link from 'next/link';
import { usePathname, useRouter } from 'next/navigation';
import { createClient } from '@/lib/supabase/client';
import type { Profile } from '@/types/database';

interface AppLayoutProps {
  children: React.ReactNode;
  profile: Profile;
}

export function AppLayout({ children, profile }: AppLayoutProps) {
  const pathname = usePathname();
  const router = useRouter();
  const supabase = useMemo(() => createClient(), []);

  const isManager = profile.role === 'manager' || profile.role === 'admin';

  const navigation = [
    { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { name: 'Calls', href: '/calls', icon: PhoneIcon },
    { name: 'Practice', href: '/practice', icon: AcademicIcon },
    { name: 'Scripts', href: '/scripts', icon: DocumentIcon },
    { name: 'Analytics', href: '/analytics', icon: ChartIcon },
  ];

  if (isManager) {
    navigation.push({ name: 'Settings', href: '/settings', icon: CogIcon });
  }

  const handleSignOut = async () => {
    await supabase.auth.signOut();
    router.push('/login');
  };

  return (
    <div className="min-h-screen bg-gray-50">
      <nav className="bg-white border-b border-gray-200">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="flex justify-between h-16">
            <div className="flex">
              <div className="flex-shrink-0 flex items-center">
                <Link href="/dashboard" className="text-2xl font-bold text-primary">
                  RepCoach
                </Link>
              </div>
              <div className="hidden sm:ml-8 sm:flex sm:space-x-4">
                {navigation.map((item) => {
                  const isActive = pathname === item.href || pathname.startsWith(item.href + '/');
                  return (
                    <Link
                      key={item.name}
                      href={item.href}
                      className={`inline-flex items-center px-3 py-2 text-sm font-medium transition-colors ${
                        isActive
                          ? 'text-primary border-b-2 border-primary'
                          : 'text-gray-600 hover:text-gray-900 hover:bg-gray-50'
                      }`}
                    >
                      <item.icon className="w-5 h-5 mr-2" />
                      {item.name}
                    </Link>
                  );
                })}
              </div>
            </div>
            <div className="flex items-center gap-4 flex-shrink-0">
              <div className="hidden md:block text-sm whitespace-nowrap">
                <span className="text-gray-500">Signed in as </span>
                <span className="font-medium text-gray-900">
                  {profile.full_name || profile.email}
                </span>
                {isManager && (
                  <span className="ml-2 badge badge-info">{profile.role}</span>
                )}
              </div>
              <button
                onClick={handleSignOut}
                className="btn-secondary btn-sm whitespace-nowrap"
              >
                Sign Out
              </button>
            </div>
          </div>
        </div>
      </nav>

      {/* Mobile navigation */}
      <div className="sm:hidden border-b border-gray-200 bg-white">
        <div className="flex overflow-x-auto px-4 py-2 gap-2">
          {navigation.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + '/');
            return (
              <Link
                key={item.name}
                href={item.href}
                className={`flex items-center px-3 py-2 text-sm font-medium whitespace-nowrap ${
                  isActive
                    ? 'text-primary bg-primary-50'
                    : 'text-gray-600 hover:text-gray-900 hover:bg-gray-50'
                }`}
              >
                <item.icon className="w-4 h-4 mr-1" />
                {item.name}
              </Link>
            );
          })}
        </div>
      </div>

      <main className="max-w-7xl mx-auto py-6 px-4 sm:px-6 lg:px-8">
        {children}
      </main>
    </div>
  );
}

